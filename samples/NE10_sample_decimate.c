/**
 * @example NE10_sample_decimate.c
 * An example of using Ne10's decimate function.
 */
int decimate_sample_main(void)
{
    ne10_uint8_t M = 3; // 48000 Hz to 16000 Hz, decimation factor is 3
    ne10_fir_decimate_instance_f32_t S;
    ne10_float32_t src[BUFFSIZE]; // A source array of input data
    ne10_float32_t dst[BUFFSIZE / M]; // A destination array for the transformed data
    ne10_float32_t st[NUMTAPS + BLOCKSIZE - 1]; // A "state" buffer for use within the FIR
    ne10_result_t status = NE10_OK;

    // Initialise Ne10, using hardware auto-detection to set library function pointers
    if (ne10_init() != NE10_OK)
    {
        fprintf(stderr, "Failed to initialise Ne10.\n");
        return 1;
    }


    status = ne10_fir_decimate_init_float (&S, NUMTAPS, M, coeffs, st, BLOCKSIZE);

    // Generate test input values
    for (int i = 0; i < BUFFSIZE; i++)
    {
        src[i] = (ne10_float32_t)rand() / RAND_MAX * 20.0f;
    }

    for (int b = 0; b < NUMBLOCKS; b++)
    {
        ne10_fir_decimate_float_c(&S, src + (b * BLOCKSIZE), dst + (b * BLOCKSIZE / M), BLOCKSIZE);
    }


    return 0;
}
